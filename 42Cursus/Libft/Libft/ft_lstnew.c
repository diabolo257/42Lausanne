#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*lst;

	lst = malloc(sizeof(*lst));
	if (lst != NULL)
	{
		lst->content = content;
		lst->next = NULL;
	}
	return (lst);
}
