#include <unistd.h>

void	ft_write_board(char *board)
{
	int	x;

	x = 0;
	while (x < 16)
	{
		write(1, &board[x], 1);
		x++;
		if (x % 4 == 0)
			write(1, "\n", 1);
		else
			write(1, " ", 1);
	}
}
